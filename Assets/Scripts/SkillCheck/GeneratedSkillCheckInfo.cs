using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class GeneratedSkillCheckInfo
{
    public float startAngle;
    public float endAngle;
    public float greatStartAngle;
    public float greatEndAngle;

    public GeneratedSkillCheckInfo(float startAngle, float endAngle, float greatStartAngle, float greatEndAngle)
    {
        this.startAngle = startAngle;
        this.endAngle = endAngle;
        this.greatStartAngle = greatStartAngle;
        this.greatEndAngle = greatEndAngle;
    }
}